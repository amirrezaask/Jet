function withSemantic(
  colors: Partial<JetTheme["colors"]>,
  highlights?: Partial<JetHighlightColors>,
): Pick<JetTheme, "colors" | "highlights"> {
  return {
    colors: { ...defaultJetTheme.colors, ...colors },
    highlights: { ...defaultJetTheme.highlights, ...highlights },
  }
}

