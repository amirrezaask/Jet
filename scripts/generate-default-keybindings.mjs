#!/usr/bin/env node
/**
 * Regenerate packages/jet-workspace/src/default-keybindings.ts from extracted VS Code macOS data.
 */
import { readFile, writeFile } from "node:fs/promises"
import { join, relative } from "node:path"
import { fileURLToPath, pathToFileURL } from "node:url"

const ROOT = join(fileURLToPath(import.meta.url), "..", "..")
const DATA = join(ROOT, "packages/jet-workspace/data")
const BINDINGS_FILE = join(DATA, "vscode-mac-keybindings.json")
const MAP_FILE = join(DATA, "jet-vscode-command-map.json")
const OUT_FILE = join(ROOT, "packages/jet-workspace/src/default-keybindings.ts")

const WHEN_BY_VSCODE = {
  "workbench.action.quickOpen": "ctx => ctx.workspaceOpen && noOverlay(ctx)",
  "workbench.action.showCommands": "noOverlay",
  "workbench.action.files.save": "ctx => ctx.workspaceOpen && noOverlay(ctx)",
  "workbench.action.files.openFile": "noOverlay",
  "workbench.action.files.openFolder": "noOverlay",
  "workbench.action.closeActiveEditor": "ctx => ctx.workspaceOpen && noOverlay(ctx)",
  "actions.find": "ctx => ctx.editorFocus && noOverlay(ctx)",
  "editor.action.startFindReplaceAction": "ctx => ctx.editorFocus && noOverlay(ctx)",
  "editor.action.gotoLine": "ctx => ctx.editorFocus && noOverlay(ctx)",
  "workbench.view.search": "ctx => ctx.workspaceOpen && noOverlay(ctx)",
  "workbench.view.scm": "ctx => ctx.workspaceOpen && noOverlay(ctx)",
  "workbench.view.explorer": "ctx => ctx.workspaceOpen && noOverlay(ctx)",
  "workbench.action.terminal.toggleTerminal": "ctx => ctx.workspaceOpen && noOverlay(ctx)",
  "workbench.actions.view.problems": "ctx => ctx.workspaceOpen && noOverlay(ctx)",
}

const JET_COMMAND_KEY = {
  "workspace.quickOpen": "quickOpen",
  "ui.showCommandPalette": "palette",
  "workspace.saveFile": "save",
  "workspace.openFile": "openFile",
  "workspace.openFolder": "openFolder",
  "layout.closeTab": "closeTab",
  "editor.find": "find",
  "editor.replace": "replace",
  "editor.gotoLine": "gotoLine",
  "search.show": "search",
  "git.showChanges": "git",
  "explorer.show": "explorer",
  "terminal.show": "terminal",
  "problems.show": "problems",
}

async function main() {
  const bindings = JSON.parse(await readFile(BINDINGS_FILE, "utf8"))
  const map = JSON.parse(await readFile(MAP_FILE, "utf8"))
  const jetByVscode = new Map(map.map(row => [row.vscode, row]))

  for (const row of map) {
    const hasExact = bindings.some(b => b.commandId === row.vscode && b.jetKey === row.key)
    if (hasExact) continue
    bindings.push({
      commandId: row.vscode,
      jetKey: row.key,
      weight: 200,
      sourceFile: "jet-vscode-command-map.json",
    })
  }

  const seen = new Set()
  const rows = []
  for (const b of bindings.sort((a, b) => b.weight - a.weight)) {
    const sig = `${b.commandId}\0${b.jetKey}`
    if (seen.has(sig)) continue
    seen.add(sig)
    rows.push(b)
  }

  const commandIds = [...new Set(rows.map(r => r.commandId))].sort()

  const bindingLines = rows.map(b => {
    const jetRow = jetByVscode.get(b.commandId)
    const cmdRef = jetRow
      ? `cmd.${JET_COMMAND_KEY[jetRow.jet] ?? jetRow.jet.split(".").pop()}`
      : `cmd[${JSON.stringify(b.commandId)}]`
    const when = WHEN_BY_VSCODE[b.commandId]
    if (when) return `  bind(${JSON.stringify(b.jetKey)}, ${cmdRef}, ${when}),`
    return `  bind(${JSON.stringify(b.jetKey)}, ${cmdRef}),`
  })

  bindingLines.unshift(
    `  bind("Cmd-n", cmd.newFile, ctx => ctx.workspaceOpen && noOverlay(ctx)),`,
  )

  const content = `// AUTO-GENERATED by scripts/generate-default-keybindings.mjs — override via keymaps.registerUser([bind(...)]).
import { anyOverlayOpen, type KeymapContext } from "./context-keys.js"
import { bind, type JetKeyBinding } from "./keymaps.js"
import type { JetCommandFn } from "./commands.js"

/** Named Jet shell commands — extend with VS Code ids via index signature. */
export type JetCommands = Record<string, JetCommandFn>

const noOverlay = (ctx: KeymapContext) => !anyOverlayOpen(ctx)

export const VSCODE_COMMAND_IDS = ${JSON.stringify(commandIds, null, 2)} as const

export function createDefaultKeybindings(cmd: JetCommands): JetKeyBinding[] {
  return [
${bindingLines.join("\n")}
  ]
}
`

  await writeFile(OUT_FILE, content)
  console.log(`Wrote ${rows.length + 1} keybindings to ${relative(ROOT, OUT_FILE)}`)
}

if (import.meta.url === pathToFileURL(process.argv[1]).href) {
  main().catch(err => {
    console.error(err)
    process.exit(1)
  })
}
